#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				result;
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		result = 0;
	else
	{
		while (i < n - 1 && str1[i] == str2[i])
			i++;
		result = str1[i] - str2[i];
	}
	return (result);
}

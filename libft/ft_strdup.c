#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		len;
	int		i;

	len = 0;
	i = 0;
	while (str[len])
		len++;
	dup = malloc(len * sizeof(char) + 1);
	if (dup == NULL)
		return (0);
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
